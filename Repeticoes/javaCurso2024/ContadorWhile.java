package javaCurso2024;

public class ContadorWhile {


	    public static void main(String[] args) {
	        int contador = 44;
	        // valor inicial

	        while (contador <= 343) { 
	        	// condição para parar o contador
	            System.out.print(contador+" ");
	            contador += 4;
	            // pula de 4 em 4
	        }
	    }
	}

	


